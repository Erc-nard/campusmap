package com.example.campusmap

import com.google.android.gms.maps.model.LatLng

enum class BuildingCategory(val displayText: String) {
    RESEARCHBUILDING(displayText = "강의·연구동"),
    DORMITORY(displayText = "기숙사"),
    WELFAREFACILITY(displayText = "복지시설"),
    STUDYPLACE(displayText = "학습공간"),
    ADMINISTRATIVE(displayText = "행정시설"),
}
data class BuildingData(
    val code: String,
    val name: String,
    val category: BuildingCategory? = null,
    val coordinates: LatLng = LatLng(0.0, 0.0),
    val polygon: List<LatLng> = listOf(),
    val keywords: List<String> = listOf(),
    val description: String = "",
    val facilityCategoryReference: Int? = null,
    val facilityItemReference: Int? = null
) {
    val buildingDescription: String
        get() = "$name ($code)"
    fun testTextQuery(query: String): Boolean {
        if (code == query)
            return true
        if (code.substringBefore('-') == query)
            return true
        if (name.contains(query))
            return true
        if (category?.displayText == query)
            return true
        if (keywords.any { it.contains(query) })
            return true
        return false
    }
}

